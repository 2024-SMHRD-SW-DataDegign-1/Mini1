package Mini1;

public class DBgm {
	
		
		private String path;
		private String title;

		
		
		
		
		public DBgm(String path, String title) {
			super();
			this.path = path;
			this.title = title;
		}



		public String getPath() {
			return path;
		}



		public String getTitle() {
			return title;
		}
		
		
		
	}